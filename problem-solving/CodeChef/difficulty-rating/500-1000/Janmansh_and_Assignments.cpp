#include <bits/stdc++.h>
#define endl "\n"
using namespace std;

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;

    while (t--)
    {
        int x;
        cin >> x;

        10 - x >= 3 ? cout << "Yes" << endl : cout << "No" << endl;
    }

    return 0;
}